func renderDSC(raw string) string {
	s := raw
	s = strings.ReplaceAll(s, "xx", lightGreen+"‚Ėą‚Ėą"+mainGreen)
	s = strings.ReplaceAll(s, "yy", darkGreen+"‚Ėą‚Ėą"+mainGreen)
	return mainGreen + s + reset
}

